    public void carregarCampos() {
        int setar = tblRelatorioCliente.getSelectedRow();

        txtNomeResult.setText(tblRelatorioCliente.getModel().getValueAt(setar, 1).toString());
        txtCodigo.setText(tblRelatorioCliente.getModel().getValueAt(setar, 0).toString());
    }
