    //método para listar os clientes
    private void listarCliente() {

        try {

            CadastroClienteDAO objcadastroClienteDAO = new CadastroClienteDAO();

            DefaultTableModel model = (DefaultTableModel) tblRelatorioCliente.getModel();
            model.setNumRows(0);

            ArrayList<cadastroClienteDTO> lista = objcadastroClienteDAO.listarCliente();
            

            for (int i = 0; i < lista.size(); i++) {
                model.addRow(new Object[]{
                    lista.get(i).getIdCPF(),
                    lista.get(i).getNome(),
                    lista.get(i).getTel(),
                    lista.get(i).getEmail(),
                    lista.get(i).getEndereco()
                });
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Listar Usuarios VIEW: " + erro);
        }
    }
    
