    private void pesquisarNome() {
        DefaultTableModel ob = (DefaultTableModel) tblRelatorioCliente.getModel();
        TableRowSorter<DefaultTableModel> obj = new TableRowSorter<>(ob);
        tblRelatorioCliente.setRowSorter(obj);
        obj.setRowFilter(RowFilter.regexFilter(txtnomePesquisar.getText()));
    }

